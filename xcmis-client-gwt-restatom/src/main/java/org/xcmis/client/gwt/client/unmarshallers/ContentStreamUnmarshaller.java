package org.xcmis.client.gwt.client.unmarshallers;

import org.xcmis.client.gwt.client.model.CmisContentStreamType;
import org.xcmis.client.gwt.client.rest.Unmarshallable;

/**
 * Document's content stream unmarshaller
 * 
 * Created by The eXo Platform SAS.
 *	
 * @author <a href="mailto:zhulevaanna@gmail.com">Ann Zhuleva</a>
 * @version $Id:   ${date} ${time}
 *
 */
public class ContentStreamUnmarshaller implements Unmarshallable
{
   /**
    * Response for CmisContentStreamType.
    */
   private CmisContentStreamType contentStreamType;

   /**
    * @param contentStreamType content stream type
    */
   public ContentStreamUnmarshaller(CmisContentStreamType contentStreamType)
   {
      this.contentStreamType = contentStreamType;
   }

   /**
    * @see org.exoplatform.gwtframework.commons.rest.Unmarshallable#unmarshal(java.lang.String)
    * 
    * @param body body
    */
   public void unmarshal(String body)
   {
      contentStreamType.setStream(body);
   }
}
